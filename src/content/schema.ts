export const CONTENT_FORMAT_VERSION = 1

// ---------------------------------------------------------------------------
// Stickers
// ---------------------------------------------------------------------------
export type StickerRarity = 'common' | 'rare' | 'epic' | 'shiny'

export interface Sticker {
  id: string
  name: string
  emoji: string
  rarity: StickerRarity
  collection: string
}

// ---------------------------------------------------------------------------
// Arcade mini-games (config-driven cards; "memory" is the built-in playable one)
// ---------------------------------------------------------------------------
export interface ArcadeGame {
  id: string
  title: string
  desc: string
  icon: string
  /** Hex/CSS background for the icon tile. */
  bg: string
  /** Two meta chips: [label, bg, color]. */
  tags: Array<[label: string, bg: string, color: string]>
  badge?: 'POPULAR' | 'NEW'
  /** Which built-in engine to launch. Only 'memory' is implemented today. */
  engine: 'memory' | 'none'
}

// ---------------------------------------------------------------------------
// Study pet
// ---------------------------------------------------------------------------
export interface PetConfig {
  species: string
  name: string
  /** Emoji per evolution stage (index = stage). */
  evolutionStages: Array<{ emoji: string; label: string }>
}

// ---------------------------------------------------------------------------
// Achievements — declarative so they are serializable / AI-generatable
// ---------------------------------------------------------------------------
export type AchievementStat =
  | 'lessonsCompleted'
  | 'streak'
  | 'competeWins'
  | 'coins'
  | 'xp'
  | 'ownedItems'
  | 'threeStarLessons'
  | 'perfectLessons'
  | 'masteredCards'
  | 'avgAccuracy'

export interface AchievementCriteria {
  stat: AchievementStat
  /** Earned when the computed stat is >= this value. */
  gte: number
  /** For avgAccuracy: require at least this many samples first. */
  minSamples?: number
}

export interface AchievementDef {
  id: string
  name: string
  emoji: string
  description: string
  criteria: AchievementCriteria
}

// ---------------------------------------------------------------------------
// App branding + reward economy
// ---------------------------------------------------------------------------
export interface AppConfig {
  title: string
  tagline: string
  /** Lesson id launched by the Home "Daily Challenge" banner. */
  dailyChallengeLessonId: string
}

export interface LevelBand {
  minLevel: number
  title: string
}

export interface EconomyConfig {
  startingCoins: number
  startingGems: number
  levels: {
    baseXp: number
    stepXp: number
    maxLevel: number
    bands: LevelBand[]
  }
  /** Combo tiers, ascending by minStreak. */
  combo: Array<{ minStreak: number; multiplier: number }>
  streakMilestones: number[]
  lesson: {
    xpPerCorrect: number
    perfectBonusXp: number
    perfectBonusGems: number
  }
  flashcard: {
    baseCoins: number
    coinsPerGotIt: number
    baseXp: number
    xpPerGotIt: number
    perfectBonusXp: number
    perfectGems: number
  }
  compete: {
    /** Index 0 = 1st place, etc. */
    placements: Array<{ coins: number; xp: number }>
    perfectGems: number
  }
  /** Lucky-spin prize table. */
  dailySpinPrizes: Array<{ coins?: number; gems?: number }>
}

export interface PackageMeta {
  id: string
  name: string
  description: string
  author: string
  version: string
  locale: string
  /** Emoji/icon for the pack picker. */
  cover: string
  createdAt?: string
}

// ---------------------------------------------------------------------------
// The package itself (authoring shape — most sections optional)
// ---------------------------------------------------------------------------
export interface ContentPackage {
  format: typeof CONTENT_FORMAT
  formatVersion: number
  meta: PackageMeta
  app?: Partial<AppConfig>
  economy?: DeepPartial<EconomyConfig>
  defaultProfile?: Partial<UserProfile>
  subjects: Subject[]
  lessons: Lesson[]
  decks?: Deck[]
  shopItems?: ShopItem[]
  themes?: Theme[]
  achievements?: AchievementDef[]
  stickers?: Sticker[]
  arcadeGames?: ArcadeGame[]
  pet?: PetConfig
}

type DeepPartial<T> = { [K in keyof T]?: T[K] extends object ? DeepPartial<T[K]> : T[K] }

// ---------------------------------------------------------------------------
// Normalized, ready-to-use content (defaults applied + index maps)
// ---------------------------------------------------------------------------
export interface LoadedContent {
  meta: PackageMeta
  app: AppConfig
  economy: EconomyConfig
  defaultProfile: UserProfile
  subjects: Subject[]
  subjectById: Record<string, Subject>
  lessons: Lesson[]
  lessonById: Record<string, Lesson>
  lessonsBySubject: Record<SubjectId, Lesson[]>
  decks: Deck[]
  deckById: Record<string, Deck>
  allCards: Flashcard[]
  cardById: Record<string, Flashcard>
  shopItems: ShopItem[]
  shopById: Record<string, ShopItem>
  themes: Theme[]
  themeById: Record<string, Theme>
  achievements: AchievementDef[]
  achievementById: Record<string, AchievementDef>
  stickers: Sticker[]
  stickerById: Record<string, Sticker>
  arcadeGames: ArcadeGame[]
  pet: PetConfig
}

