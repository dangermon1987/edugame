// ---------------------------------------------------------------------------
// Validation
// ---------------------------------------------------------------------------
export interface ValidationResult {
  ok: boolean
  errors: string[]
  warnings: string[]
}

export function validateContentPackage(input: unknown): ValidationResult {
  const errors: string[] = []
  const warnings: string[] = []
  const pkg = input as Partial<ContentPackage> | null

  if (!pkg || typeof pkg !== 'object') {
    return { ok: false, errors: ['Package is not an object'], warnings }
  }
  if (pkg.format !== CONTENT_FORMAT) {
    errors.push(`"format" must be "${CONTENT_FORMAT}" (got ${JSON.stringify(pkg.format)})`)
  }
  if (typeof pkg.formatVersion !== 'number') {
    errors.push('"formatVersion" must be a number')
  } else if (pkg.formatVersion > CONTENT_FORMAT_VERSION) {
    warnings.push(`Package formatVersion ${pkg.formatVersion} is newer than supported ${CONTENT_FORMAT_VERSION}`)
  }
  if (!pkg.meta || typeof pkg.meta.id !== 'string' || !pkg.meta.id) {
    errors.push('"meta.id" is required')
  }
  if (!pkg.meta || typeof pkg.meta.name !== 'string' || !pkg.meta.name) {
    errors.push('"meta.name" is required')
  }
  if (!Array.isArray(pkg.subjects) || pkg.subjects.length === 0) {
    errors.push('"subjects" must be a non-empty array')
  }
  if (!Array.isArray(pkg.lessons) || pkg.lessons.length === 0) {
    errors.push('"lessons" must be a non-empty array')
  }

  // Referential integrity (warnings — the app tolerates them).
  if (Array.isArray(pkg.subjects) && Array.isArray(pkg.lessons)) {
    const subjectIds = new Set(pkg.subjects.map((s) => s.id))
    for (const l of pkg.lessons) {
      if (!subjectIds.has(l.subjectId)) {
        warnings.push(`Lesson "${l.id}" references unknown subject "${l.subjectId}"`)
      }
      if (!Array.isArray(l.questions) || l.questions.length === 0) {
        errors.push(`Lesson "${l.id}" has no questions`)
      } else {
        for (const q of l.questions) {
          if (!Array.isArray(q.options) || q.options.length < 2) {
            errors.push(`Question "${q.id}" in lesson "${l.id}" needs >= 2 options`)
          } else if (q.correctIndex < 0 || q.correctIndex >= q.options.length) {
            errors.push(`Question "${q.id}" correctIndex out of range`)
          }
        }
      }
    }
  }
  if (Array.isArray(pkg.decks)) {
    for (const d of pkg.decks) {
      if (!Array.isArray(d.cards) || d.cards.length === 0) {
        warnings.push(`Deck "${d.id}" has no cards`)
      }
    }
  }

  return { ok: errors.length === 0, errors, warnings }
}

// ---------------------------------------------------------------------------
// Normalization — apply defaults and build index maps
// ---------------------------------------------------------------------------
function mergeEconomy(partial?: DeepPartial<EconomyConfig>): EconomyConfig {
  const d = DEFAULT_ECONOMY
  if (!partial) return structuredCloneSafe(d)
  return {
    startingCoins: partial.startingCoins ?? d.startingCoins,
    startingGems: partial.startingGems ?? d.startingGems,
    levels: {
      baseXp: partial.levels?.baseXp ?? d.levels.baseXp,
      stepXp: partial.levels?.stepXp ?? d.levels.stepXp,
      maxLevel: partial.levels?.maxLevel ?? d.levels.maxLevel,
      bands: (partial.levels?.bands as LevelBand[]) ?? d.levels.bands,
    },
    combo: (partial.combo as EconomyConfig['combo']) ?? d.combo,
    streakMilestones: (partial.streakMilestones as number[]) ?? d.streakMilestones,
    lesson: { ...d.lesson, ...partial.lesson },
    flashcard: { ...d.flashcard, ...partial.flashcard },
    compete: {
      placements: (partial.compete?.placements as EconomyConfig['compete']['placements']) ?? d.compete.placements,
      perfectGems: partial.compete?.perfectGems ?? d.compete.perfectGems,
    },
    dailySpinPrizes: (partial.dailySpinPrizes as EconomyConfig['dailySpinPrizes']) ?? d.dailySpinPrizes,
  }
}

function structuredCloneSafe<T>(v: T): T {
  return JSON.parse(JSON.stringify(v)) as T
}

function indexBy<T extends { id: string }>(items: T[]): Record<string, T> {
  return Object.fromEntries(items.map((i) => [i.id, i]))
}

export function normalizeContentPackage(pkg: ContentPackage): LoadedContent {
  const subjects = pkg.subjects
  const lessons = [...pkg.lessons].sort((a, b) => a.order - b.order)
  const decks = pkg.decks ?? []
  const shopItems = pkg.shopItems ?? []
  const themes = pkg.themes ?? [{ id: '', name: 'Default', emoji: '🎨', description: 'Default look', swatch: ['#6C5CE7', '#A29BFE', '#FECA57'] }]
  const achievements = pkg.achievements ?? []
  const stickers = pkg.stickers ?? []
  const arcadeGames = pkg.arcadeGames ?? DEFAULT_ARCADE
  const allCards = decks.flatMap((d) => d.cards)

  const lessonsBySubject = {} as Record<SubjectId, Lesson[]>
  for (const s of subjects) lessonsBySubject[s.id] = []
  for (const l of lessons) {
    ;(lessonsBySubject[l.subjectId] ??= []).push(l)
  }

  const app: AppConfig = { ...DEFAULT_APP, ...pkg.app }
  if (!app.dailyChallengeLessonId && lessons[0]) app.dailyChallengeLessonId = lessons[0].id

  return {
    meta: pkg.meta,
    app,
    economy: mergeEconomy(pkg.economy),
    defaultProfile: { ...DEFAULT_PROFILE, ...pkg.defaultProfile },
    subjects,
    subjectById: indexBy(subjects),
    lessons,
    lessonById: indexBy(lessons),
    lessonsBySubject,
    decks,
    deckById: indexBy(decks),
    allCards,
    cardById: indexBy(allCards),
    shopItems,
    shopById: indexBy(shopItems),
    themes,
    themeById: indexBy(themes),
    achievements,
    achievementById: indexBy(achievements),
    stickers,
    stickerById: indexBy(stickers),
    arcadeGames,
    pet: pkg.pet ?? DEFAULT_PET,
  }
}
