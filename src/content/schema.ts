/**
 * ============================================================================
 * EduQuest Content Package format (v1)
 * ============================================================================
 *
 * A *content package* is a single JSON document that supplies ALL course
 * content to the app: subjects, lessons + questions, flashcard decks + cards,
 * the reward shop, themes, achievements, stickers, arcade games, the study
 * pet, app branding, and the reward economy.
 *
 * The app ships a built-in core package, can load packages from a static
 * `/content` folder, from an uploaded file, or from Google Drive. Different
 * packages = different courses, with no code changes. The format is designed to
 * be generated by an AI: everything is plain data (achievements use declarative
 * criteria rather than code), most sections have sensible defaults, and a small
 * package (e.g. just subjects + lessons) is valid — `normalizeContentPackage`
 * fills the rest.
 *
 * User progress (currency, XP, lesson/card progress, owned items) is NOT part
 * of a content package; it lives in the user-state store and syncs separately.
 */
